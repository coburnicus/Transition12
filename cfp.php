<h2 id="cfp">Call for Proposals</h2>
<p>The 2012 National Transition Conference will take place from May 30-June 1, 2012 in Washington D.C. This conference will bring together partners in the transition community, including young adults and family members.</p>

<p>All conference participants will have the opportunity to exchange innovative ideas and approaches; demonstrate knowledge gained from policy implementation, share transition practices and research findings and promote and facilitate the development of networks and relationships in order to promote practice, policy and research that leads to improved employment outcomes and self sufficiency for young people with disabilities.</p>
<p>The Conference Planning Committee is seeking proposals for presentations and posters that align with the six theme tracks selected for the conference:</p>
<ul>
<li>Employment</li>
<li>Family engagement</li>
<li>Partnerships that support successful transitions</li>
<li>Program and policy development and implementation </li>
<li>Secondary and postsecondary education</li>
<li>Youth development</li>
</ul>

<p>All proposals must be related directly to the conference topic of transition for young people with disabilities with a focus on paths to careers including training and post secondary education. For both presentations and poster session, the Committee encourages submissions from young people and family members who have personal experience with the transition process as well as the inclusion of young adults and family members on panels and presentation teams.</p>

<p>Each presentation should include at least two of the following components or focus areas: success stories, engagement of young people and families, approaches to transition planning, strategies to support retention in services and education, evidence based and emerging practices, research and/or policy. Presenters are encouraged to consider approaches that will actively engage the audience.</p>

<p>For additional information go to www.transition2012.org. All proposals must be submitted using the online submission process.</p>

<p>If you have questions about the Call for Proposals for the 2012 National Transition Conference please contact Cindy Thomas at cindy.thomas@umb.edu or 617-287-4312 or
Melanie Jordan at melanie.jordan@umb.edu at 617-287-4327.</p>

