<h1>About Washington, DC</h1>
<p>Few places in the world are as easily recognizable as Washington, DC.  Just the mention of our nation's capitol conjures images of monuments and museums, power and intrigue, and, the ongoing, ever-changing great American experiment in representative democracy. This is especially true as we enter another presidential election year.</p>

<p>As America's second most visited city (after New York), Washington's list of superlatives are nearly endless.  Getting around town is relatively easy and inexpensive, thanks to the Metro Rail system (<a href="http://www.wmata.com/rail/">http://www.wmata.com/rail/</a>).  Most key tourist sites are near a station, and, our conference hotel is adjacent to the Woodley Park-Zoo/Adams Morgan Station (Red Line).</p>

<p><strong>Washington DC</strong> Old Town Trolley Tours (Leaves from Union Station (Red Line) or 10th& E Streets NW) <a href="http://www.trolleytours.com/washington-dc/">www.trolleytours.com/washington-dc/</a> or 202.832.9800 x0: Tour runs 9am-5:30 pm, daily, every half an hour, admission prices available online or at kiosk.  Please call 24 hours in advance to reserve a trolley with a lift.  This is a “hop-on, hop off” tour and will take you to many of the sights on this list.</p> 

<p><strong>United States Capitol</strong>: Site of stirring inaugurations, moving tributes to fallen leaders, great compromises, and, not an insignificant amount of political foolishness, the Capitol is more than just the setting of Dan Brown's The Lost Symbol – it is where the people and power intersect.  (<a href="http://www.aoc.gov/cc/visit/index.cfm">http://www.aoc.gov/cc/visit/index.cfm</a>).</p>

<p><strong>White House</strong>: Every school child in America knows the address – 1600 Pennsylvania Avenue.  Now it's your turn to see where 43 of our 44 First Families have lived.   (<a href="http://www.whitehouse.gov/about/tours-and-events">http://www.whitehouse.gov/about/tours-and-events</a>).</p>

<p><strong>Supreme Court</strong>: Interested in where our laws are declared constitutional – or not?  The Supreme Court is a must see for any budding lawyers. (<a href="http://www.supremecourt.gov/visiting/visitorservices.aspx">http://www.supremecourt.gov/visiting/visitorservices.aspx</a>).</p>

<p><strong>Library of Congress</strong>: Washington's most beautiful building, the Library of Congress is well known to National Treasure fans as the home of the Presidents' Book of Secrets.  With amazing architecture and fascinating exhibits, you won't want to miss this jewel.  (<a href="http://www.loc.gov/visit/">http://www.loc.gov/visit/</a>)</p>

<p><strong>The Pentagon</strong>: Make sure you're wearing good walking shoes for this tour of the world's largest office building.  Your visit will include a stop by a memorial chapel set up to remember the victims of the 9/11 attack.  Remember to visit the outdoor memorial, as well.  (<a href="http://pentagon.afis.osd.mil/">http://pentagon.afis.osd.mil/</a>)</p>

<p><strong>National Archives</strong>: Few things are as inspiring as gazing upon original copies of America's founding documents, including the Constitution, the Declaration of Independence, and the Bill of Rights.  (<a href="http://www.archives.gov/nae/">http://www.archives.gov/nae/</a>)</p>

<p><strong>Smithsonian Museums</strong>: Free, fun and informative, the Smithsonian museums surround the National Mall and extend far beyond.  Make sure to do your homework before you visit.  Lindbergh's “Spirit of St. Louis”, Dorothy's ruby slippers, and, the renowned Hope diamond are all here – but all in separate museums.  Plan wisely!  (Blue/Orange Lines) 202.633.1000 (voice/tape) or 202.633.5285 (TTY). Most museums open daily, 10am-5:30pm (<a href="http://www.si.edu/Museums">http://www.si.edu/Museums</a>)

<p><strong>Monuments</strong>: The Lincoln Memorial, the Vietnam Memorial Wall – they are among our nation's most well known landmarks which are on and around the National Mall.  Most are accessible by a short walk from a Metro Rail Station, though you may want to investigate a one-day pass from one of the tour tram operators if you plan to visit more far-flung monuments, such as the one in memory of Franklin Roosevelt.   Keep in mind that the Washington Monument may still be closed or under renovation due to damage from the 2011 earthquake.</p>

<p><strong>National Zoo</strong>: A short walk from our hotel, the Zoo, like all Smithsonian units, is free.  If you haven't ever seen a giant panda up close, here's your chance! 202.633.4800; 202.673.7800 (TTY) 10am-6pm (<a href="http://nationalzoo.si.edu/">http://nationalzoo.si.edu/</a>)</p>

<p><strong>Also of interest</strong>: Washington has too many attractions to list here, but some additional sites you might find interesting include Ford's Theatre (<a href="http://fordstheatre.org/">http://fordstheatre.org/</a>), the Newseum (<a href="http://www.newseum.org/">http://www.newseum.org/</a>), Washington National Cathedral (<a href="http://www.nationalcathedral.org/">http://www.nationalcathedral.org/</a>), the International Spy Museum (<a href="http://www.spymuseum.org/">http://www.spymuseum.org/</a>) and, the National Museum of Crime and Punishment (<a href="http://www.crimemuseum.org/">http://www.crimemuseum.org/</a>), just to name a few.  </p>
