<p>The National Youth Transitions Center (NYTC) is a shared nonprofit building developed by The HSC Foundation that brings together the resources of multiple organizations to provide transition-related services, research, public policy, best practices, innovative projects, and evaluation so that youth with disabilities can achieve their greatest level of independence. A discreet program within the NYTC, the National Veterans Center focuses on supporting young wounded veterans transition back to civilian life and post-secondary education and employment. Together, the Centers enhance the ability of young people and young veterans with disabilities to enter and re-enter the world of work. The Centers are guided by a National Youth Transitions Collaborative, a group of 42 national, regional and local organizations that provide expertise and advice on planning, programming, and advocacy. We hope you will join us for a guided tour on this green building that combines historical architecture with modern accessible design.</p>


