<h2 id="sr">Requirements for Proposal Submission</h2>
<p>Proposals must be submitted electronically at www.transition2012.org/
proposal</p>

<p><strong>Title (100 characters or less)</strong>
Provide the full title for your presentation or poster. This title will be included in the conference program if your proposal is selected.</p>

<p><strong>Proposal Abstract (500 characters or less)</strong>
The abstract should provide an overview of your topic and the goals of your presentation. If your proposal is selected the abstract will be included in the conference program.</p>

<p><strong>Theme Track</strong>
Select the theme track that best represents the content of your proposal.</p>

<p><strong>Presentation Format</strong>
The conference will offer concurrent 75 minute and 90 minute breakout sessions as well as a poster session. Presenters are asked to select their preferred format. The Conference Committee does reserve the option
to recommend an alternative if necessary in development of the conference schedule. Presentations should include time for questions and answers.</p>

<p><strong>Presentation Components</strong>
Each presentation should include at least two of the following components or focus areas: success stories, engagement of young people and families, approaches to transition planning, strategies to support retention in services and education, evidence based and emerging practices, research, and/or policy.</p>

<p><strong>Proposal Summary (300 words or less)</strong>
This summary will be used by the review panel to evaluate your proposal. In addition to providing an overview of the topic, the summary should help the reader understand the significance and relevance of the topic
and how this presentation will inform practices or policy, and how the presenters will effectively engage the audience.</p>

<p><strong>Presenter Information</strong>
Name, organization/affiliation, brief bio and contact information must be provided for each presenter. Only the presenters name and organization/affiliation will be listed in the conference program.</p>

<p><strong>Presenter Agreement</strong>
By submitting a proposal to the 2012 National Transition Conference presenters acknowledge the following:</p>
<ul>
<li>All proposal content must use People First language.</li>
<li>Presenters are responsible for their own travel and conference registration expenses. A limited number
of scholarships may be available for young people with disabilities and family members who do not
have access to other sources of support.</li>
<li>Presenters are responsible for providing the presentation content in alternative formats (e.g. electronic
file, large print, captioned video).</li>
<li>Presenters will have the option to submit their presentation materials for posting on the conference
website after the conference. Materials provided should be in an accessible format.</li>
<li>An LCD projector, screen and power supply will be provided for each concurrent session. Presenters are
responsible for providing or paying the cost of any additional audiovisual equipment that is needed.</li>
<li>Each poster session station will include a 6’x30” table and chair. A board for posting materials and
power supply can be available if requested in advance.</li>
</ul>


