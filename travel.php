<h2>AIRPORTS</h2>
<p>When making your air reservations, please note that Washington, DC is served by three major airports, Ronald Reagan Washington National Airport (DCA), Baltimore Washington Airport (BWI) and Dulles Airport (AID).  Ronald Reagan (DCA) is by far the closest to our conference hotel, and is served by the Metro Rail system.</p>

