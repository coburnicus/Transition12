<p>These taxicab companies have wheelchair-accessible vehicles as well as traditional vehicles. Patrons should call the cab company to request a wheelchair-accessible cab. Passengers are encouraged to call ahead whenever possible. These companies provide specialized training to the drivers who provide this service. The fare is set at the meter rate, and the service is available 24 hours per day, 7 days per week. Cabs may be requested up to 2 weeks in advance. Call the individual cab company for information or to arrange for service.</p>

