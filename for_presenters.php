<h1>Presenter Guidelines and Tips</h1>

<p>Thank you for agreeing to present at the conference. We appreciate your willingness to share your knowledge and experience with attendees. To ensure an efficient and effective conference, please consider the following guidelines.</p>

<ol>
	<li>Make your materials as accessible as possible:
<ul>		
<li>Please bring a least one copy of handouts in large print format: 20-point, sans-serif font (like Arial or Helvetica) with at least 1.25 spacing between lines. Avoid italics or hyphens at the right margin, and format the text flush left.</li>

<li>Use 12-point font or larger for all other handouts, and minimize the use of colored print or paper. Black on white provides the best contrast for handouts.</li>

<li>In designing PowerPoint presentations, use at least 24-point type. Use short phrases, not lengthy text.  Use high contrast colors between background and letters. Use pictures / graphics, along with words, to deliver your message. Be sure to communicate all the information verbally, in order that everyone may fully participate.</li>

<li>Consult the following sites for detailed information about making PowerPoint presentations accessible:
<a href="http://office.microsoft.com/en-us/support/creating-accessible-powerpoint-presentations-HA102013555.aspx">http://office.microsoft.com/en-us/support/creating-accessible-powerpoint-presentations-HA102013555.aspx</a> 
<a href="http://webaim.org/techniques/powerpoint/">http://webaim.org/techniques/powerpoint/ </a></li>

<li>Provide audience members with copies of your presentation. This will not only help attendees follow along in your presentation, but will save you if there are any technology snafus.</li>

<li>Have available at least two copies of your materials on CD for audience members who request alternate formats.  If you are unable to provide this, please ask for names and addresses and mail / email them after the conference.</li>
</ul>
</li>

<li>People vary in their learning styles and preferences. Organize your presentation so that it uses a variety of formats – auditory (verbal presentations), visual (PowerPoint, pictures, video), and written (handouts).  Engage the audience – pose questions to stimulate discussion, consider using short exercises, stories and examples are wonderful teaching tools.</li>

<li>Keep the language in your presentation as simple and clear as possible. Attendees include a wide range of individuals from across the country. Avoid jargon and acronyms.</li>

<li>Use language that opens doors for people with disabilities by describing them in terms of their abilities and strengths. Use “people-first” language (e.g., people with disabilities, not “the disabled”).</li>

<li>Don’t feel you have to cover every detail of your topic. Time for your session will pass quickly. Keep your presentation content manageable and focused. Be sure to leave time for questions.</li>

<li>Each workshop will have a session monitor, who can facilitate room set-up or A/V issues as well as provide assistance to the presenter as needed. </li>

<li>Here are some additional suggestions for a successful presentation:
<ul>
<li>If you tend to speak quickly or softly, please slow down and speak up.</li>
<li>Face the audience when speaking and keep hands away from your mouth so those who read lips can understand you; do not speak while facing flip chart or screen.</li>
<li>Repeat all questions asked by the audience before answering.</li>
<li>If an attendee with a question has difficulty speaking, wait for the individual to finish. If you don’t understand, ask the person to repeat the comment. If you still don’t understand, restate what you did comprehend and ask for more information.</li>
<li>If sign language interpreters are used, face the person using the interpreter, not the interpreter. Phrase your answers as you would for others. Do not stand or walk between interpreters and their audience.</li>
<li>Pay close attention to time, especially if there are multiple presenters. If you are concerned with managing time, the room monitor can assist.</li>
<li>Relax and enjoy the interaction – we all have so much to learn from each other!</li>
</ul>
</li>
</ol>



